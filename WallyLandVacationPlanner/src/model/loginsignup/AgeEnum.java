package model.loginsignup;

/**
 * AgeEnum represents different age categories for classification purposes.
 * Each constant represents a general age group.
 * 
 * <ul>
 *   <li>toddler - Typically for ages 1-3, representing very young children.</li>
 *   <li>child - Typically for ages 3-18, representing early childhood to pre-adolescence.</li>
 *   <li>adult - Represents mature individuals, generally ages 18-65.</li>
 *   <li>senior - Represents older adults, generally age 65 and above.</li>
 * </ul>
 * 
 * This enumeration can be used to categorize individuals based on age in applications
 * such as healthcare, entertainment, or educational services.
 * 
 * @author Ana
 */
public enum AgeEnum {
    toddler,
    child,
    adult,
    senior
}
